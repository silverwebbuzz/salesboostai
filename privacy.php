      <h1 class="text-gradient">Privacy Policy</h1>
      <p>
        At <strong>SWB SalesBoost AI</strong>, we value your privacy and are committed to protecting merchant and store data.
        This Privacy Policy explains how we collect, use, and safeguard information when you install and use our Shopify app.
      </p>

      <h2>1. Information We Collect</h2>
      <p>When you install and use SWB SalesBoost AI, we may collect:</p>
      <ul>
        <li>Store information (shop domain, store name, owner details provided by Shopify)</li>
        <li>Commerce data required for analytics (orders, products, customers, inventory signals)</li>
        <li>App usage data (feature usage, plan details, sync status, and settings)</li>
        <li>Technical data required for secure operation (API logs, webhook metadata, error diagnostics)</li>
      </ul>
      <p>We do not collect or store customer payment card details.</p>

      <h2>2. How We Use Your Information</h2>
      <p>We use collected information to:</p>
      <ul>
        <li>Provide analytics dashboards, alerts, and AI insights inside the app</li>
        <li>Maintain subscription, billing, and entitlement functionality</li>
        <li>Support data sync, webhook processing, and app reliability</li>
        <li>Improve product performance and customer support</li>
      </ul>
      <p>We do not sell your data to third parties.</p>

      <h2>3. Data Sharing</h2>
      <p>
        We only share data with trusted service providers as needed to operate the app (for example, infrastructure and monitoring),
        or when required by law.
      </p>

      <h2>4. Data Storage &amp; Security</h2>
      <p>
        We use reasonable administrative, technical, and organizational safeguards to protect data against unauthorized access,
        alteration, disclosure, or destruction.
      </p>

      <h2>5. Data Retention</h2>
      <p>
        We retain store data while your store actively uses the app. After uninstall, data is deleted or anonymized in line with
        Shopify requirements and applicable legal obligations.
      </p>

      <h2>6. Your Rights</h2>
      <p>Depending on your jurisdiction, you may have rights to:</p>
      <ul>
        <li>Request access to data associated with your store</li>
        <li>Request correction or deletion of eligible data</li>
        <li>Uninstall the app to revoke future access</li>
      </ul>

      <h2>7. Shopify Compliance Webhooks</h2>
      <p>
        SWB SalesBoost AI supports Shopify privacy compliance processes, including customer data requests and redact workflows,
        through required compliance webhook endpoints.
      </p>

